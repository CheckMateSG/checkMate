export enum TIME {
  // 1 minute in milliseconds
  ONE_MINUTE = 60 * 1000,
  // 10 minutes in milliseconds
  TEN_MINUTES = 10 * 60 * 1000,
  // 1 hour in milliseconds
  ONE_HOUR = 60 * 60 * 1000,
  // 24 hours in milliseconds
  ONE_DAY = 24 * 60 * 60 * 1000,
  // 30 days in milliseconds
  THIRTY_DAYS = 30 * 24 * 60 * 60 * 1000,
}
